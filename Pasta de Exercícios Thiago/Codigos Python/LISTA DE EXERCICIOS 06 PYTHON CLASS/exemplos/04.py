class Carro:
    def __init__(self,nome,marca,peso,cambio,aro,monstruosidade):
        self.nome = nome
        self.marca = marca
        self.peso = peso
        self.cambio = cambio
        self.aro = aro
        self.monstruosidade = monstruosidade

    def nomedacarro(self):
        print(f"Nome: {self.nome}")

    def modelcarro(self):
        print(f"Marca: {self.marca}") 

    def pesodalenda(self):
        print(f"Peso: {self.peso} toneladas")

    def cambiocarro(self):
        print(f"Cambio: {self.cambio}") 

    def arodaroda(self):
        print(f"Aro da roda: {self.aro}") 

    def monstro(self):
        print(f"Monstruosidade: {self.monstruosidade}")

